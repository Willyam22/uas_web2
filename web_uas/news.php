                    puncture.</p>

                <p>The Briton enjoyed a 10-second lead heading into lap three as Van der Poel and Van Aert co-operated
                    in the chase. He
                    wouldn't be racing off into the distance, though, as his decision to ride up the mid-lap hill while
                    his rivals ran saw
                    him shed time and – on the third lap – lose his lead as the two chasers caught back up.</p>
                <p>"I'm maybe a little bit disappointed," Pidcock said after the race. "It was a very heavy race. The
                    last few laps I
