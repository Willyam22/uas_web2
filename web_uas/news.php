                    race, though it
                    wouldn't be a straightforward win for the Dutchman, even if Van Aert was stuck further back after
                    the start.</p>

                <p>"Actually, for me, I never felt like being in the mix for victory today," Van Aert said. "From the
                    start, I was chasing.
                    I was never in the rhythm I wanted to have. I must be happy today with second place. It was the
                    highest possible for me.</p>

                <p>"It was still a really nice day out. There were so many crowds, and it was like a World Championships
